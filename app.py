# Импортируем класс Flask из модуля flask
from flask import Flask

# Создаем экземпляр Flask-приложения
# __name__ - имя текущего модуля (используется Flask для нахождения ресурсов)
app = Flask(__name__)

# Декоратор route определяет URL-путь ('/') для вызова функции
@app.route('/')
def hello():
    """Обработчик корневого маршрута, возвращает простое приветствие"""
    return "Hello from Docker!"

# Проверяем, что скрипт запущен напрямую, а не импортирован
if __name__ == '__main__':
    # Запускаем веб-сервер:
    # host='0.0.0.0' - сервер доступен со всех сетевых интерфейсов
    # port=5000 - сервер слушает на порту 5000
    app.run(host='0.0.0.0', port=5000)
